package com.employee.source.model;

/**
 * @author Vinod
 *
 */
public class EmployeeModel {

	private String employeeId;
	private String employeeRole;
	private Integer employeeVacationDays;
	private Integer employeeWorkDays;
	
	
	public String getEmployeeId() {
		return employeeId;
	}
	public void setEmployeeId(String employeeId) {
		this.employeeId = employeeId;
	}
	public String getEmployeeRole() {
		return employeeRole;
	}
	public void setEmployeeRole(String employeeRole) {
		this.employeeRole = employeeRole;
	}
	public Integer getEmployeeVacationDays() {
		return employeeVacationDays;
	}
	public void setEmployeeVacationDays(Integer employeeVacationDays) {
		this.employeeVacationDays = employeeVacationDays;
	}
	public Integer getEmployeeWorkDays() {
		return employeeWorkDays;
	}
	public void setEmployeeWorkDays(Integer employeeWorkDays) {
		this.employeeWorkDays = employeeWorkDays;
	}
	
	
	
}
